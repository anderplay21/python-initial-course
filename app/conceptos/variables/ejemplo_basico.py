# Ejemplo basico de variables

# Una variable guarda un valor.
nombre = "Ana"
edad = 20
altura = 1.65
es_estudiante = True

print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("Es estudiante:", es_estudiante)

# Puedes cambiar el valor de una variable.
edad = 21
print("Nueva edad:", edad)
